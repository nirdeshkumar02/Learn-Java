package com.company;

public class Ch_5_tut_2 {
    public static void main(String[] args) {
        // ---------------------------------- Do - While Loops ----------------------------------//

//        do {
//            //code
//        } while (condition);

        int a = 0;
        while(a<5){
            System.out.println(a);
            a++;
        }
        int b = 10;
        do {
            System.out.println(b);
            b++;
        }while(b<5);

        int c = 1;
        do{
            System.out.println(c);
            c++;
        }while(c<=45);
    }
}
